            sb.Append($"{awaitPrefix}{containingType}.{method.MethodName}({args})");
        }
        else if (method.IsOnAggregate)
        {
            var containingType = Fqn(method.Symbol.ContainingType);
            sb.Append($"{awaitPrefix}{containingType}.{method.MethodName}({args})");
