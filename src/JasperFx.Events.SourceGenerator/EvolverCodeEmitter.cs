    private static void EmitSyncEvolveMethod(StringBuilder sb, EvolveMethodInfo evolve, INamedTypeSymbol aggregateType,
        string docType, string idType)
    {
        var ctorExpression = BuildAggregateConstructorExpression(aggregateType);
        sb.AppendLine($"    public {docType}? Evolve({docType}? snapshot, {idType} id, global::JasperFx.Events.IEvent e)");
        sb.AppendLine("    {");

        if (evolve.IsMutable)
        {
            // void Evolve(object) or void Evolve(IEvent)
            sb.AppendLine($"        snapshot ??= {ctorExpression};");
            if (evolve.TakesIEvent)
            {
                sb.AppendLine("        snapshot.Evolve(e);");
            }
            else
            {
                sb.AppendLine("        snapshot.Evolve(e.Data);");
            }
            sb.AppendLine("        return snapshot;");
        }
        else
        {
            // TDoc Evolve(object) or TDoc Evolve(IEvent)
            if (evolve.TakesIEvent)
            {
                sb.AppendLine($"        return (snapshot ?? {ctorExpression}).Evolve(e);");
            }
            else
            {
                sb.AppendLine($"        return (snapshot ?? {ctorExpression}).Evolve(e.Data);");
            }
        }

        sb.AppendLine("    }");
    }

    private static void EmitAsyncEvolveMethod(StringBuilder sb, EvolveMethodInfo evolve, INamedTypeSymbol aggregateType,
        string docType, string idType)
    {
        var ctorExpression = BuildAggregateConstructorExpression(aggregateType);
        sb.AppendLine($"    public async global::System.Threading.Tasks.ValueTask<{docType}?> EvolveAsync(");
        sb.AppendLine($"        {docType}? snapshot, {idType} id, global::JasperFx.Events.IEvent e,");
        sb.AppendLine("        object session, global::System.Threading.CancellationToken ct)");
        sb.AppendLine("    {");

        // Build the method call arguments
        var args = new List<string>();
        if (evolve.TakesIEvent)
            args.Add("e");
        else
            args.Add("e.Data");

        if (evolve.HasSession)
        {
            // Cast the session parameter to the user's declared session type
            var sessionParam = evolve.Symbol.Parameters.FirstOrDefault(p =>
                IsQuerySession(p.Type));
            var sessionTypeName = sessionParam != null
                ? Fqn(sessionParam.Type)
                : "object";
            args.Add($"({sessionTypeName})session");
        }

        if (evolve.HasCancellationToken)
            args.Add("ct");

        var argsStr = string.Join(", ", args);
        var methodName = evolve.Symbol.Name; // "Evolve" or "EvolveAsync"

        if (evolve.IsMutable)
        {
            sb.AppendLine($"        snapshot ??= {ctorExpression};");
            sb.AppendLine($"        await snapshot.{methodName}({argsStr});");
            sb.AppendLine("        return snapshot;");
        }
        else
        {
            sb.AppendLine($"        return await (snapshot ?? {ctorExpression}).{methodName}({argsStr});");
        }

        sb.AppendLine("    }");
    }

    private static bool IsQuerySession(ITypeSymbol type)
    {
        var name = type.ToDisplayString();
        return name.EndsWith("IQuerySession") || name.EndsWith("QuerySession");
    }

    /// <summary>
    /// Constructor expression for the aggregate when the SG needs to build a
    /// fresh instance in the Apply-only / null-snapshot branch. Three cases:
    ///
    /// 1. Public parameterless ctor, no required members → `new T()`.
    /// 2. Public parameterless ctor with required members →
    ///    `new T { Required = default!, ... }`. The C# compiler accepts the
    ///    construction even though required members are about to be overwritten by Apply.
    /// 3. No public parameterless ctor (e.g. `private T()` signalling
    ///    "construct me reflectively" or no parameterless ctor at all) →
    ///    `(T)RuntimeHelpers.GetUninitializedObject(typeof(T))`. Allocates
    ///    the instance without invoking any ctor or field initializers. Pre-#276
    ///    Marten used `Activator.CreateInstance(nonPublic: true)` for this
    ///    pattern; GetUninitializedObject is the AOT-clean equivalent.
    ///    Caveat: field initializers don't run — aggregates that rely on
    ///    them must move initialization into Apply / Create.
    ///
    /// See #297 + Marten 9.0 migration guide.
    /// </summary>
    private static string BuildAggregateConstructorExpression(INamedTypeSymbol type)
    {
        var fqn = Fqn(type);
        var requiredMembers = new List<string>();
        // Dedup by name: an `override required` (or `new`-shadowed required) member
        // appears in both the derived type's and the base type's GetMembers(), which
        // would otherwise emit `new T { Prop = default!, Prop = default! }` -> CS1912.
        // The walk runs most-derived -> base, so the first sighting wins.
        var seen = new HashSet<string>(StringComparer.Ordinal);
        for (INamedTypeSymbol? current = type;
             current is not null && current.SpecialType != SpecialType.System_Object;
             current = current.BaseType)
        {
            foreach (var member in current.GetMembers())
            {
                if (member is IPropertySymbol prop && prop.IsRequired)
                {
                    if (seen.Add(prop.Name)) requiredMembers.Add(prop.Name);
                }
                else if (member is IFieldSymbol field && field.IsRequired)
                {
                    if (seen.Add(field.Name)) requiredMembers.Add(field.Name);
                }
            }
        }

        var hasPublicParameterless = type.TypeKind == TypeKind.Struct ||
            type.InstanceConstructors.Any(c =>
                c.Parameters.Length == 0 && c.DeclaredAccessibility == Accessibility.Public);

        if (hasPublicParameterless && requiredMembers.Count > 0)
        {
            var inits = string.Join(", ", requiredMembers.Select(n => $"{n} = default!"));
            return $"new {fqn} {{ {inits} }}";
        }

        if (hasPublicParameterless) return $"new {fqn}()";

        // Reflective-instantiation fallback for `private T()` / no-parameterless-ctor
        // aggregates. Field initializers won't run; document in the migration guide.
        return $"({fqn})global::System.Runtime.CompilerServices.RuntimeHelpers.GetUninitializedObject(typeof({fqn}))";
    }

    /// <summary>
    /// Builds an evolver class name that includes the parent-type chain so two
    /// aggregates with the same simple name (e.g. `Bug_2666.CounterWithCreate`
    /// and `Bug_2528.CounterWithCreate`, common in xUnit test files where the
    /// aggregate is nested inside the test class) don't collide on the
    /// generated `CounterWithCreateEvolver` class name in the shared namespace.
    /// See #297.
    /// </summary>
    private static string BuildUniqueEvolverName(CandidateInfo info, string suffix)
    {
        var prefix = new StringBuilder();
        for (INamedTypeSymbol? container = info.ClassSymbol.ContainingType;
             container is not null;
             container = container.ContainingType)
        {
            prefix.Insert(0, container.Name + "_");
        }

        // When the same aggregate type can be registered against multiple
        // stream-identity types (e.g. `AggregateStreamAsync<CountOfLetters>`
        // with both Guid and string stream IDs), emit a separate evolver per
        // TId. Append a sanitized TId tag in the name so the two evolvers
        // don't collide in the same namespace. Only needed when the aggregate
        // *itself* doesn't determine TId via an Id property — when it does,
        // Pipeline 1 emits a single dispatcher per TDoc and existing tests
        // (and downstream consumers) reference the original
        // `{Name}Evolver` class name. See #297.
        var idTag = "";
        if (info.IdentityType is { } id && !AggregateHasOwnIdentity(info))
        {
            var idName = id.Name;
            if (!string.IsNullOrEmpty(idName))
                idTag = "_" + idName.Replace(".", "_").Replace("<", "_").Replace(">", "_").Replace(",", "_").Replace(" ", "");
        }

        return prefix.ToString() + info.ClassSymbol.Name + idTag + suffix;
    }

    /// <summary>
    /// True when the aggregate type itself locks in the TId (via an Id property
    /// or [AggregateIdentity] attribute). False when the candidate's TId came
    /// from a Pipeline 3 call-site hint and could conceivably vary by call site.
    /// </summary>
    private static bool AggregateHasOwnIdentity(CandidateInfo info)
    {
        if (info.AggregateType is null) return false;

        for (INamedTypeSymbol? current = info.AggregateType;
             current is not null && current.SpecialType != SpecialType.System_Object;
             current = current.BaseType)
        {
            foreach (var member in current.GetMembers())
            {
                if (member is IPropertySymbol prop && prop.Name == "Id")
                    return true;
            }

            foreach (var attr in current.GetAttributes())
            {
                if (attr.AttributeClass?.Name is "AggregateIdentityAttribute" or "AggregateIdentity")
                    return true;
            }
        }

        return false;
    }

    private static string GetFullyQualifiedEvolverName(CandidateInfo info, string evolverName)
    {
        var ns = info.ClassSymbol.ContainingNamespace;
        if (!ns.IsGlobalNamespace)
            return $"global::{ns.ToDisplayString()}.{evolverName}";
        return $"global::{evolverName}";
    }

