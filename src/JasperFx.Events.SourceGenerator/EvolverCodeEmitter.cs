    private static void EmitSelfAggregatingCreateCallAsync(StringBuilder sb, ConventionalMethodInfo method,
        string dataVar)
    {
        var args = BuildSelfAggregatingArgsAsync(method, dataVar);
        var awaitPrefix = method.IsAsync ? "await " : "";

        if (method.IsStatic)
        {
            var containingType = Fqn(method.Symbol.ContainingType);
            sb.Append($"{awaitPrefix}{containingType}.{method.MethodName}({args})");
        }
        else
        {
            sb.Append($"{awaitPrefix}snapshot.{method.MethodName}({args})");
        }
    }

    private static void EmitSelfAggregatingApplyCallAsync(StringBuilder sb, ConventionalMethodInfo method,
        string dataVar)
    {
        var args = BuildSelfAggregatingArgsAsync(method, dataVar);
        var awaitPrefix = method.IsAsync ? "await " : "";
        var receiver = method.IsStatic
            ? Fqn(method.Symbol.ContainingType)
            : "snapshot";

        if (method.ReturnsAggregate)
        {
            sb.AppendLine($"snapshot = {awaitPrefix}{receiver}.{method.MethodName}({args});");
        }
        else
        {
            sb.AppendLine($"{awaitPrefix}{receiver}.{method.MethodName}({args});");
        }
    }

    /// <summary>
    /// Variant of <see cref="BuildSelfAggregatingArgs"/> for the async emit
    /// path — routes IQuerySession-typed params to `typedSession` (the cast
    /// local emitted at the top of EvolveAsync) instead of `session`.
    /// </summary>
    private static string BuildSelfAggregatingArgsAsync(ConventionalMethodInfo method, string dataVar)
    {
        var parts = new List<string>();
        var parameters = method.Symbol.Parameters;
        var containingType = method.Symbol.ContainingType;

        foreach (var param in parameters)
        {
            var paramType = param.Type;

            if (method.UsesIEventWrapper && IsIEventGenericWrapper(paramType))
            {
                var innerType = Fqn(((INamedTypeSymbol)paramType).TypeArguments[0]);
                parts.Add($"(global::JasperFx.Events.IEvent<{innerType}>)e");
            }
            else if (IsRawIEvent(paramType))
            {
                parts.Add("e");
            }
            else if (SymbolEqualityComparer.Default.Equals(paramType, containingType))
            {
                parts.Add("snapshot");
            }
            else if (IsQuerySession(paramType))
            {
                parts.Add("typedSession");
            }
            else if (IsCancellationToken(paramType))
            {
                parts.Add("cancellation");
            }
            else
            {
                parts.Add(dataVar);
            }
        }

        return string.Join(", ", parts);
    }

    // --- Self-aggregating: DetermineAction (sync, with ShouldDelete) ---

    private static void EmitSelfAggregatingDetermineAction(StringBuilder sb, CandidateInfo info, string docType,
        string idType)
    {
        sb.AppendLine(
            $"    public ({docType}?, global::JasperFx.Events.Daemon.ActionType) DetermineAction({docType}? snapshot, {idType} id, global::System.Collections.Generic.IReadOnlyList<global::JasperFx.Events.IEvent> events)");
        sb.AppendLine("    {");
        sb.AppendLine("        var exists = snapshot != null;");
        sb.AppendLine("        foreach (var e in events)");
        sb.AppendLine("        {");
        // Per-event try/catch (parallel to EmitDetermineActionAsyncOverride, same
        // rationale — preserve the per-event seam for the daemon's dead-letter routing).
        // See #305.
        sb.AppendLine("            try");
        sb.AppendLine("            {");
        sb.AppendLine("            switch (e.Data)");
        sb.AppendLine("            {");

        // ShouldDelete - set snapshot to null and continue processing (allows re-creation)
        var shouldDeleteMethods = info.Methods.Where(m => m.MethodName == "ShouldDelete").ToList();
        foreach (var method in shouldDeleteMethods)
        {
            var eventTypeName = Fqn(method.EventType);
            sb.AppendLine($"                case {eventTypeName} data:");
            sb.Append("                    if (snapshot != null && ");
            EmitSelfAggregatingShouldDeleteCall(sb, method, "data");
            sb.AppendLine(")");
            sb.AppendLine("                        snapshot = null;");
            sb.AppendLine("                    break;");
        }

        // Create + Apply
        var createMethods = info.Methods.Where(m => m.MethodName == "Create").ToList();
        var applyMethods = info.Methods.Where(m => m.MethodName == "Apply").ToList();

        // Group by event type (excluding ShouldDelete events already handled)
        var handledDeleteTypes = new HashSet<ITypeSymbol>(shouldDeleteMethods.Select(m => m.EventType),
            SymbolEqualityComparer.Default);

        var applyAndCreateTypes = info.Methods
            .Where(m => m.MethodName != "ShouldDelete")
            .Select(m => m.EventType)
            .Distinct<ITypeSymbol>(SymbolEqualityComparer.Default)
            .Where(t => !handledDeleteTypes.Contains(t!))
            .OrderByDescending(t => GetTypeDepth(t!))
            .ToList();

        foreach (var eventType in applyAndCreateTypes)
        {
            var eventTypeName = Fqn(eventType!);
            var creates = createMethods.Where(m =>
                SymbolEqualityComparer.Default.Equals(m.EventType, eventType)).ToList();
            var applies = applyMethods.Where(m =>
                SymbolEqualityComparer.Default.Equals(m.EventType, eventType)).ToList();

            sb.AppendLine($"                case {eventTypeName} data:");

            if (creates.Count > 0)
            {
                var create = creates[0];
                sb.AppendLine("                    if (snapshot == null)");
                sb.Append("                        snapshot = ");
                EmitSelfAggregatingCreateCall(sb, create, "data");
                sb.AppendLine(";");

                if (applies.Count > 0)
                {
                    sb.AppendLine("                    else");
                    sb.Append("                        ");
                    EmitSelfAggregatingApplyCall(sb, applies[0], "data");
                }
            }
            else if (applies.Count > 0)
            {
                if (info.HasDefaultConstructor)
                {
                    sb.AppendLine(
                        $"                    snapshot ??= {BuildAggregateConstructorExpression(info.AggregateType!)};");
                }
                else
                {
                    sb.AppendLine("                    if (snapshot == null) break;");
                }

                sb.Append("                    ");
                EmitSelfAggregatingApplyCall(sb, applies[0], "data");
            }

            sb.AppendLine("                    break;");
        }

        sb.AppendLine("            }");
        sb.AppendLine("            }");
        sb.AppendLine("            catch (global::JasperFx.Events.Daemon.ApplyEventException)");
        sb.AppendLine("            {");
        sb.AppendLine("                throw;");
        sb.AppendLine("            }");
        sb.AppendLine("            catch (global::System.Exception __ex)");
        sb.AppendLine("                when (!global::JasperFx.Events.Projections.ProjectionExceptions.IsExceptionTransient(__ex))");
        sb.AppendLine("            {");
        sb.AppendLine("                throw new global::JasperFx.Events.Daemon.ApplyEventException(e, __ex);");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();
        sb.AppendLine("        if (snapshot == null)");
        sb.AppendLine(
            "            return exists ? (null, global::JasperFx.Events.Daemon.ActionType.Delete) : (null, global::JasperFx.Events.Daemon.ActionType.Nothing);");
        sb.AppendLine($"        return (snapshot, global::JasperFx.Events.Daemon.ActionType.Store);");
        sb.AppendLine("    }");
    }

    // --- Null snapshot case generation (sync) ---

    private static void EmitNullSnapshotCases(StringBuilder sb, CandidateInfo info,
        List<ConventionalMethodInfo> createMethods, List<ConventionalMethodInfo> applyMethods, string indent,
        bool dispatchOnThis = false)
    {
        // For each event type with a Create method
        foreach (var method in createMethods)
        {
            var eventTypeName = Fqn(method.EventType);
            if (method.UsesIEventWrapper)
            {
                sb.AppendLine($"{indent}case {eventTypeName}:");
                sb.Append($"{indent}    return ");
                EmitCreateCallSync(sb, method, null, "e", dispatchOnThis);
            }
            else
            {
                sb.AppendLine($"{indent}case {eventTypeName} data:");
                sb.Append($"{indent}    return ");
                EmitCreateCallSync(sb, method, "data", "e", dispatchOnThis);
            }

            sb.AppendLine(";");
        }

        // For Apply-only event types (no matching Create), use default ctor + Apply
        var createEventTypes = new HashSet<ITypeSymbol>(createMethods.Select(m => m.EventType),
            SymbolEqualityComparer.Default);

        foreach (var method in applyMethods)
        {
            if (createEventTypes.Contains(method.EventType)) continue;

            var eventTypeName = Fqn(method.EventType);
            var docType = Fqn(info.AggregateType!);

            if (!info.HasDefaultConstructor) continue; // Can't create without constructor

            if (method.UsesIEventWrapper)
            {
                sb.AppendLine($"{indent}case {eventTypeName}:");
            }
            else
            {
                sb.AppendLine($"{indent}case {eventTypeName} data:");
            }

            var ctorExpression = BuildAggregateConstructorExpression(info.AggregateType!);
            if (method.IsVoid && !method.ReturnsAggregate)
            {
                sb.AppendLine($"{indent}{{");
                sb.AppendLine($"{indent}    var s = {ctorExpression};");
                sb.Append($"{indent}    ");
                EmitApplyCallExpression(sb, method, method.UsesIEventWrapper ? null : "data", "e", false, "s", dispatchOnThis);
                sb.AppendLine(";");
                sb.AppendLine($"{indent}    return s;");
                sb.AppendLine($"{indent}}}");
            }
            else
            {
                // Returns aggregate - construct into a local first. Using the constructor
                // expression directly as the Apply receiver breaks for the reflective
                // `(T)GetUninitializedObject(...)` fallback: `.Apply(...)` binds tighter than
                // the cast, so Apply resolves against `object` -> CS1061 (#505).
                sb.AppendLine($"{indent}{{");
                sb.AppendLine($"{indent}    var s = {ctorExpression};");
                sb.Append($"{indent}    return ");
                EmitApplyCallExpression(sb, method, method.UsesIEventWrapper ? null : "data", "e", false, "s", dispatchOnThis);
                sb.AppendLine(";");
                sb.AppendLine($"{indent}}}");
            }
        }
    }

    // --- Null snapshot case generation (async) ---

    private static void EmitNullSnapshotCasesAsync(StringBuilder sb, CandidateInfo info,
        List<ConventionalMethodInfo> createMethods, List<ConventionalMethodInfo> applyMethods, string indent,
        bool dispatchOnThis = false)
    {
        foreach (var method in createMethods)
        {
            var eventTypeName = Fqn(method.EventType);
            if (method.UsesIEventWrapper)
            {
                sb.AppendLine($"{indent}case {eventTypeName}:");
                sb.Append($"{indent}    return ");
            }
            else
            {
                sb.AppendLine($"{indent}case {eventTypeName} data:");
                sb.Append($"{indent}    return ");
            }

            EmitCreateCall(sb, method, method.UsesIEventWrapper ? null : "data", "e", true, dispatchOnThis);
            sb.AppendLine(";");
        }

        var createEventTypes = new HashSet<ITypeSymbol>(createMethods.Select(m => m.EventType),
            SymbolEqualityComparer.Default);

        foreach (var method in applyMethods)
        {
            if (createEventTypes.Contains(method.EventType)) continue;
            if (!info.HasDefaultConstructor) continue;

            var eventTypeName = Fqn(method.EventType);
            var docType = Fqn(info.AggregateType!);

            sb.AppendLine($"{indent}case {eventTypeName} data:");
            sb.AppendLine($"{indent}{{");
            sb.AppendLine($"{indent}    var s = {BuildAggregateConstructorExpression(info.AggregateType!)};");
            var awaitPrefix = method.IsAsync ? "await " : "";
            sb.Append($"{indent}    {awaitPrefix}");
            EmitApplyCallExpression(sb, method, "data", "e", true, "s", dispatchOnThis);
            sb.AppendLine(";");
            sb.AppendLine($"{indent}    return s;");
            sb.AppendLine($"{indent}}}");
        }
    }

    // --- Non-null snapshot case generation (sync) ---

    private static void EmitNonNullSnapshotCases(StringBuilder sb, CandidateInfo info,
        List<ConventionalMethodInfo> applyMethods, string indent, bool dispatchOnThis = false)
    {
        foreach (var method in applyMethods)
        {
            var eventTypeName = Fqn(method.EventType);

            if (method.UsesIEventWrapper)
            {
                sb.AppendLine($"{indent}case {eventTypeName}:");
            }
            else
            {
                sb.AppendLine($"{indent}case {eventTypeName} data:");
            }

            if (method.IsVoid && !method.ReturnsAggregate)
            {
                sb.Append($"{indent}    ");
                EmitApplyCallExpression(sb, method, method.UsesIEventWrapper ? null : "data", "e", false, "snapshot", dispatchOnThis);
                sb.AppendLine(";");
                sb.AppendLine($"{indent}    return snapshot;");
            }
            else
            {
                sb.Append($"{indent}    return ");
                EmitApplyCallExpression(sb, method, method.UsesIEventWrapper ? null : "data", "e", false, "snapshot", dispatchOnThis);
                sb.AppendLine(";");
            }
        }
    }

    // --- Non-null snapshot case generation (async) ---

    private static void EmitNonNullSnapshotCasesAsync(StringBuilder sb, CandidateInfo info,
        List<ConventionalMethodInfo> applyMethods, string indent, bool dispatchOnThis = false)
    {
        foreach (var method in applyMethods)
        {
            var eventTypeName = Fqn(method.EventType);
            sb.AppendLine($"{indent}case {eventTypeName} data:");

            if (method.IsAsync)
            {
                if (method.ReturnsAggregate)
                {
                    sb.Append($"{indent}    return await ");
                    EmitApplyCallExpression(sb, method, "data", "e", true, "snapshot", dispatchOnThis);
                    sb.AppendLine(";");
                }
                else
                {
                    sb.Append($"{indent}    await ");
                    EmitApplyCallExpression(sb, method, "data", "e", true, "snapshot", dispatchOnThis);
                    sb.AppendLine(";");
                    sb.AppendLine($"{indent}    return snapshot;");
                }
            }
            else
            {
                // Sync method body inside the async dispatch path — `session` and
                // `cancellation` are still in scope from the enclosing
                // EvolveAsync / DetermineActionAsync method, so handler params
                // that need them must still bind correctly. Pass
                // includeSessionAndCancellation=true here. See #297.
                if (method.ReturnsAggregate)
                {
                    sb.Append($"{indent}    return ");
                    EmitApplyCallExpression(sb, method, "data", "e", true, "snapshot", dispatchOnThis);
                    sb.AppendLine(";");
                }
                else
                {
                    sb.Append($"{indent}    ");
                    EmitApplyCallExpression(sb, method, "data", "e", true, "snapshot", dispatchOnThis);
                    sb.AppendLine(";");
                    sb.AppendLine($"{indent}    return snapshot;");
                }
            }
        }
    }

    // --- Call expression helpers ---

    private static void EmitApplyCallExpression(StringBuilder sb, ConventionalMethodInfo method,
        string? dataVar, string eventVar, bool includeSessionAndCancellation, string aggregateVar = "snapshot",
        bool dispatchOnThis = false)
    {
        var args = BuildApplyArgs(method, dataVar, eventVar, includeSessionAndCancellation, aggregateVar);

        if (method.IsOnAggregate)
        {
            sb.Append($"{aggregateVar}.{method.MethodName}({args})");
        }
        else if (method.IsStatic)
        {
            // Static Apply declared on the projection itself — invoke via the projection type.
            sb.Append($"{Fqn(method.Symbol.ContainingType)}.{method.MethodName}({args})");
        }
        else
        {
            // Instance Apply on the projection — dispatched through the evolver's held projection
            // instance for the file-scoped dispatcher (default path, #462) or directly on `this` when
            // the override lives on the user's partial projection class (the DI-safe path,
            // marten#4787, see EmitPartialProjectionWithDIOverride).
            sb.Append($"{InstanceCallPrefix(dispatchOnThis)}{method.MethodName}({args})");
        }
    }

    private static void EmitCreateCallSync(StringBuilder sb, ConventionalMethodInfo method,
        string? dataVar, string eventVar, bool dispatchOnThis = false)
    {
        var args = BuildCreateArgs(method, dataVar, eventVar, false);

        if (method.IsStatic)
        {
            var containingType = Fqn(method.Symbol.ContainingType);
            sb.Append($"{containingType}.{method.MethodName}({args})");
        }
        else if (method.IsOnAggregate)
        {
            // Static create on aggregate
            var containingType = Fqn(method.Symbol.ContainingType);
            sb.Append($"{containingType}.{method.MethodName}({args})");
        }
        else
        {
            sb.Append($"{InstanceCallPrefix(dispatchOnThis)}{method.MethodName}({args})");
        }
    }

    private static void EmitCreateCall(StringBuilder sb, ConventionalMethodInfo method,
        string? dataVar, string eventVar, bool isAsync, bool dispatchOnThis = false)
    {
        var args = BuildCreateArgs(method, dataVar, eventVar, isAsync);
        var awaitPrefix = method.IsAsync && isAsync ? "await " : "";

        if (method.IsStatic)
        {
            var containingType = Fqn(method.Symbol.ContainingType);
            sb.Append($"{awaitPrefix}{containingType}.{method.MethodName}({args})");
        }
        else if (method.IsOnAggregate)
        {
            var containingType = Fqn(method.Symbol.ContainingType);
            sb.Append($"{awaitPrefix}{containingType}.{method.MethodName}({args})");
        }
        else
        {
            sb.Append($"{awaitPrefix}{InstanceCallPrefix(dispatchOnThis)}{method.MethodName}({args})");
        }
    }

    private static void EmitApplyCallStatement(StringBuilder sb, ConventionalMethodInfo method,
        string dataVar, string eventVar, bool isAsync, bool dispatchOnThis = false)
    {
        if (method.IsAsync && isAsync)
        {
            if (method.ReturnsAggregate)
            {
                sb.Append("snapshot = await ");
            }
            else
            {
                sb.Append("await ");
            }
        }
        else
        {
            if (method.ReturnsAggregate)
            {
                sb.Append("snapshot = ");
            }
        }

        EmitApplyCallExpression(sb, method, dataVar, eventVar, isAsync, dispatchOnThis: dispatchOnThis);
        sb.AppendLine(";");
    }

    private static void EmitShouldDeleteCall(StringBuilder sb, ConventionalMethodInfo method, string dataVar,
        bool dispatchOnThis = false)
    {
        var args = BuildShouldDeleteArgs(method, dataVar);
        var awaitKeyword = method.IsAsync ? "await " : "";

        if (method.IsOnAggregate)
        {
            sb.Append($"{awaitKeyword}snapshot.{method.MethodName}({args})");
        }
        else if (method.IsStatic)
        {
            sb.Append($"{awaitKeyword}{Fqn(method.Symbol.ContainingType)}.{method.MethodName}({args})");
        }
        else
        {
            sb.Append($"{awaitKeyword}{InstanceCallPrefix(dispatchOnThis)}{method.MethodName}({args})");
        }
    }

    private static string BuildSelfAggregatingArgs(ConventionalMethodInfo method, string dataVar)
    {
        var parts = new List<string>();
        var parameters = method.Symbol.Parameters;
        var containingType = method.Symbol.ContainingType;

        foreach (var param in parameters)
        {
            var paramType = param.Type;

            if (method.UsesIEventWrapper && IsIEventGenericWrapper(paramType))
            {
                var innerType = Fqn(((INamedTypeSymbol)paramType).TypeArguments[0]);
                parts.Add($"(global::JasperFx.Events.IEvent<{innerType}>)e");
            }
            else if (IsRawIEvent(paramType))
            {
                parts.Add("e");
            }
            else if (SymbolEqualityComparer.Default.Equals(paramType, containingType))
            {
                // The aggregate itself, as a second/third parameter on a self-aggregating
                // Apply/Create/ShouldDelete method — e.g.
                //   public TAgg Apply(SomeEvent e, TAgg current) => ...;
                // The previous emitter unconditionally fell through to `dataVar` for any
                // non-IEvent parameter, generating `snapshot.Apply(data, data)` (CS1503).
                parts.Add("snapshot");
            }
            else if (IsQuerySession(paramType))
            {
                parts.Add("session");
            }
            else if (IsCancellationToken(paramType))
            {
                parts.Add("cancellation");
            }
            else
            {
                // Event data
                parts.Add(dataVar);
            }
        }

        return string.Join(", ", parts);
    }

    private static void EmitSelfAggregatingCreateCall(StringBuilder sb, ConventionalMethodInfo method,
        string dataVar)
    {
        var args = BuildSelfAggregatingArgs(method, dataVar);

        if (method.IsStatic)
        {
            var containingType = Fqn(method.Symbol.ContainingType);
            sb.Append($"{containingType}.{method.MethodName}({args})");
        }
        else
        {
            // Instance Create on aggregate - not typical for self-aggregating
            sb.Append($"snapshot.{method.MethodName}({args})");
        }
    }

    private static void EmitSelfAggregatingApplyCall(StringBuilder sb, ConventionalMethodInfo method,
        string dataVar)
    {
        var args = BuildSelfAggregatingArgs(method, dataVar);

        // Static Apply on a self-aggregating record (e.g.
        //   public static TAgg Apply(SomeEvent e, TAgg current) => ...;
        // ) — must be invoked as Type.Apply(...) not snapshot.Apply(...) (CS0176).
        var receiver = method.IsStatic
            ? Fqn(method.Symbol.ContainingType)
            : "snapshot";

        if (method.ReturnsAggregate)
        {
            sb.AppendLine($"snapshot = {receiver}.{method.MethodName}({args});");
        }
        else
        {
            sb.AppendLine($"{receiver}.{method.MethodName}({args});");
        }
    }

    private static void EmitSelfAggregatingShouldDeleteCall(StringBuilder sb, ConventionalMethodInfo method,
        string dataVar)
    {
        var args = BuildSelfAggregatingArgs(method, dataVar);
        var receiver = method.IsStatic
            ? Fqn(method.Symbol.ContainingType)
            : "snapshot";
        sb.Append($"{receiver}.{method.MethodName}({args})");
    }

    // --- Argument builders ---

    private static string BuildApplyArgs(ConventionalMethodInfo method, string? dataVar, string eventVar,
        bool includeSessionAndCancellation, string aggregateVar = "snapshot")
    {
        var parts = new List<string>();
        var parameters = method.Symbol.Parameters;

        foreach (var param in parameters)
        {
            var paramType = param.Type;

            if (method.UsesIEventWrapper && IsIEventGenericWrapper(paramType))
            {
                var innerType = Fqn(((INamedTypeSymbol)paramType).TypeArguments[0]);
                parts.Add($"(global::JasperFx.Events.IEvent<{innerType}>){eventVar}");
            }
            else if (IsRawIEvent(paramType))
            {
                parts.Add(eventVar);
            }
            else if (IsQuerySession(paramType) && includeSessionAndCancellation)
            {
                parts.Add("session");
            }
            else if (IsCancellationToken(paramType) && includeSessionAndCancellation)
            {
                parts.Add("cancellation");
            }
            else if (IsAggregateParam(paramType, method))
            {
                parts.Add(aggregateVar);
            }
            else
            {
                // Event data
                parts.Add(dataVar ?? $"(({Fqn(paramType)})e.Data)");
            }
        }

        return string.Join(", ", parts);
    }

    private static string BuildCreateArgs(ConventionalMethodInfo method, string? dataVar, string eventVar,
        bool includeSessionAndCancellation)
    {
        var parts = new List<string>();
        var parameters = method.Symbol.Parameters;

