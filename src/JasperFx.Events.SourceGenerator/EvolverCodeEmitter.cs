            }
            else if (IsQuerySession(paramType) && includeSessionAndCancellation)
            {
                parts.Add("session");
            }
            else if (IsCancellationToken(paramType) && includeSessionAndCancellation)
            {
                parts.Add("cancellation");
            }
            else
            {
                // Event data
                parts.Add(dataVar ?? $"(({Fqn(paramType)})e.Data)");
            }
