    /// <summary>
    /// marten#4787: ShouldDelete-aware partial-class override. Matches the virtual at
    /// <c>JasperFxAggregationProjectionBase.Runtime.cs:93</c>:
    /// <c>public virtual ValueTask&lt;(TDoc?, ActionType)&gt; DetermineActionAsync(TQuerySession session, TDoc? snapshot, TId identity, IIdentitySetter&lt;TDoc, TId&gt; identitySetter, IReadOnlyList&lt;IEvent&gt; events, CancellationToken cancellation)</c>.
    /// Body mirrors the file-scoped <see cref="EmitDetermineActionEvolverMethod"/> verbatim, with
    /// <c>dispatchOnThis: true</c> swapping every <c>_projection.</c> instance call for a bare call
    /// on <c>this</c>. Per-event ApplyEventException wrapping is preserved because the runtime
    /// dispatches DetermineActionAsync as a single call (no <c>evolveDefaultAsync</c>-style per-event
    /// catch wrapper above us).
    /// </summary>
    private static void EmitDetermineActionAsOverride(StringBuilder sb, CandidateInfo info)
    {
        var docType = Fqn(info.AggregateType!);
        var idType = Fqn(info.IdentityType!);
        var querySessionType = Fqn(info.QuerySessionType!);
        var isAsync = info.HasAnyAsync;
        var asyncKeyword = isAsync ? "async " : "";

        sb.AppendLine("    [global::System.CodeDom.Compiler.GeneratedCodeAttribute(\"JasperFx.Events.SourceGenerator\", \"1.0\")]");
        sb.AppendLine($"    public override {asyncKeyword}global::System.Threading.Tasks.ValueTask<({docType}?, global::JasperFx.Events.Daemon.ActionType)> DetermineActionAsync(");
        sb.AppendLine($"        {querySessionType} session,");
        sb.AppendLine($"        {docType}? snapshot,");
        sb.AppendLine($"        {idType} identity,");
        sb.AppendLine($"        global::JasperFx.Events.Aggregation.IIdentitySetter<{docType}, {idType}> identitySetter,");
        sb.AppendLine($"        global::System.Collections.Generic.IReadOnlyList<global::JasperFx.Events.IEvent> events,");
        sb.AppendLine($"        global::System.Threading.CancellationToken cancellation)");
        sb.AppendLine("    {");
        sb.AppendLine("        var exists = snapshot != null;");
        sb.AppendLine("        foreach (var e in events)");
        sb.AppendLine("        {");
        sb.AppendLine("            try");
        sb.AppendLine("            {");
        sb.AppendLine("            switch (e.Data)");
        sb.AppendLine("            {");

        var shouldDeleteMethods = info.Methods.Where(m => m.MethodName == "ShouldDelete").ToList();
        var shouldDeleteByEventType = shouldDeleteMethods
            .GroupBy<ConventionalMethodInfo, ITypeSymbol>(m => m.EventType, SymbolEqualityComparer.Default)
            .OrderByDescending(g => GetTypeDepth(g.Key))
            .ToList();
        var shouldDeleteEventTypes = new HashSet<ITypeSymbol>(
            shouldDeleteMethods.Select(m => m.EventType), SymbolEqualityComparer.Default);
        var ctorDeleteEventTypes = info.ConstructorDeleteEventTypes
            .Where(t => !shouldDeleteEventTypes.Contains(t))
            .Distinct<ITypeSymbol>(SymbolEqualityComparer.Default)
            .OrderByDescending(t => GetTypeDepth(t))
            .ToList();

        foreach (var group in shouldDeleteByEventType)
        {
            var eventTypeName = Fqn(group.Key);
            var dataVar = "data";
            sb.AppendLine($"                case {eventTypeName} {dataVar}:");
            sb.Append("                    if (snapshot != null && (");
            bool first = true;
            foreach (var method in group)
            {
                if (!first) sb.Append(" || ");
                first = false;
                EmitShouldDeleteCall(sb, method, dataVar, dispatchOnThis: true);
            }
            sb.AppendLine("))");
            sb.AppendLine("                        snapshot = null;");
            sb.AppendLine("                    break;");
        }
        foreach (var eventType in ctorDeleteEventTypes)
        {
            sb.AppendLine($"                case {Fqn(eventType)}:");
            sb.AppendLine("                    snapshot = null;");
            sb.AppendLine("                    break;");
        }

        var createMethods = CoalesceByEventType(info.Methods.Where(m => m.MethodName == "Create"));
        foreach (var method in createMethods)
        {
            var eventTypeName = Fqn(method.EventType);
            sb.AppendLine($"                case {eventTypeName} data when snapshot == null:");
            sb.Append("                    snapshot = ");
            EmitCreateCall(sb, method, "data", "e", isAsync, dispatchOnThis: true);
            sb.AppendLine(";");
            sb.AppendLine("                    break;");
        }

        var applyMethods = CoalesceByEventType(info.Methods.Where(m => m.MethodName == "Apply"));
        foreach (var method in applyMethods)
        {
            var eventTypeName = Fqn(method.EventType);
            bool hasCreateForType = createMethods.Any(c =>
                SymbolEqualityComparer.Default.Equals(c.EventType, method.EventType));

            if (hasCreateForType)
            {
                sb.AppendLine($"                case {eventTypeName} data when snapshot != null:");
            }
            else if (info.HasDefaultConstructor)
            {
                sb.AppendLine($"                case {eventTypeName} data:");
                sb.AppendLine($"                    snapshot ??= {BuildAggregateConstructorExpression(info.AggregateType!)};");
            }
            else
            {
                sb.AppendLine($"                case {eventTypeName} data when snapshot != null:");
            }

            sb.Append("                    ");
            EmitApplyCallStatement(sb, method, "data", "e", isAsync, dispatchOnThis: true);
            sb.AppendLine("                    break;");
        }

        sb.AppendLine("            }");
        sb.AppendLine("            }");
        sb.AppendLine("            catch (global::JasperFx.Events.Daemon.ApplyEventException)");
        sb.AppendLine("            {");
        sb.AppendLine("                throw;");
        sb.AppendLine("            }");
        sb.AppendLine("            catch (global::System.Exception __ex)");
        sb.AppendLine("                when (!global::JasperFx.Events.Projections.ProjectionExceptions.IsExceptionTransient(__ex))");
        sb.AppendLine("            {");
        sb.AppendLine("                throw new global::JasperFx.Events.Daemon.ApplyEventException(e, __ex);");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();
        sb.AppendLine("        if (snapshot == null)");
        if (!isAsync)
        {
            sb.AppendLine($"            return exists ? new global::System.Threading.Tasks.ValueTask<({docType}?, global::JasperFx.Events.Daemon.ActionType)>((null, global::JasperFx.Events.Daemon.ActionType.Delete)) : new global::System.Threading.Tasks.ValueTask<({docType}?, global::JasperFx.Events.Daemon.ActionType)>((null, global::JasperFx.Events.Daemon.ActionType.Nothing));");
            sb.AppendLine($"        return new global::System.Threading.Tasks.ValueTask<({docType}?, global::JasperFx.Events.Daemon.ActionType)>((snapshot, global::JasperFx.Events.Daemon.ActionType.Store));");
        }
        else
        {
            sb.AppendLine($"            return exists ? (null, global::JasperFx.Events.Daemon.ActionType.Delete) : (null, global::JasperFx.Events.Daemon.ActionType.Nothing);");
            sb.AppendLine($"        return (snapshot, global::JasperFx.Events.Daemon.ActionType.Store);");
        }
        sb.AppendLine("    }");
    }

    /// <summary>
    /// Builds the expression that creates the evolver's private "shadow" projection instance
    /// (used only to invoke the projection's stateless instance Apply/Create/ShouldDelete event
    /// methods). When the projection has a public parameterless constructor we just <c>new</c> it.
    /// A DI-activated projection (registered via <c>AddProjectionWithServices</c>) has only a
    /// constructor with injected dependencies and therefore no parameterless ctor — <c>new T()</c>
    /// would not compile (#4185 / CS7036). In that case instantiate without running the constructor
    /// (mirrors the aggregate no-parameterless-ctor fallback in
    /// <see cref="BuildAggregateConstructorExpression"/>). Note: the DI-projection path is no longer
    /// reached for partial projections — see <see cref="EmitPartialProjectionWithDIOverride"/> for the
    /// alternative emission used when convention methods need DI-injected dependencies at runtime
    /// (marten#4787). This shadow-instance path is preserved only as a defensive fallback (e.g. a
    /// non-partial projection that somehow reaches this branch) — generated dispatch through such a
    /// shadow would still NRE on any deref of an injected service, but the new emission path
    /// covers the supported partial-projection case correctly.
    /// </summary>
    private static string BuildProjectionInstanceExpression(INamedTypeSymbol projectionType, string projectionFullName)
    {
        if (HasPublicParameterlessCtor(projectionType))
        {
            return $"new {projectionFullName}()";
        }

        return $"({projectionFullName})global::System.Runtime.CompilerServices.RuntimeHelpers.GetUninitializedObject(typeof({projectionFullName}))";
    }

    /// <summary>
    /// True when the projection class declares a <c>public</c> parameterless constructor (or has no
    /// explicit constructors, so the implicit default counts). The opposite (a projection whose only
    /// public ctor takes DI-resolved parameters) is the trigger for the partial-class override emission
    /// path that fixes marten#4787 — dispatching events through a <c>RuntimeHelpers.GetUninitializedObject</c>
    /// shadow leaves injected fields null and NREs the first time a convention method dereferences one.
    /// </summary>
    private static bool HasPublicParameterlessCtor(INamedTypeSymbol projectionType)
    {
        return projectionType.InstanceConstructors.Any(c =>
            c.Parameters.Length == 0 && c.DeclaredAccessibility == Accessibility.Public);
    }

    /// <summary>
    /// Prefix used when emitting a call to an instance convention method on the projection. Inside the
    /// file-scoped Evolver dispatcher it's <c>_projection.</c> (the shadow instance held by the Evolver).
    /// Inside a partial-class override on the user's projection itself it's empty — the call binds to
    /// <c>this</c> implicitly, which is the DI-resolved instance with injected fields populated. See
    /// <see cref="EmitPartialProjectionWithDIOverride"/> and marten#4787.
    /// </summary>
    private static string InstanceCallPrefix(bool dispatchOnThis) => dispatchOnThis ? string.Empty : "_projection.";

    /// <summary>
    /// The IGeneratedAsyncEvolver / IGeneratedAsyncDetermineAction contracts type the session as
    /// <c>object</c> so the runtime stays free of the session generic parameter. The dispatch body
    /// references a strongly-typed <c>session</c> local when (and only when) a handler actually takes an
    /// IQuerySession — emitting an unused local otherwise would trip CS0219 in warnings-as-error consumer
    /// builds. Mirrors the self-aggregating async path.
    /// </summary>
    private static void EmitSessionLocalIfNeeded(StringBuilder sb, CandidateInfo info)
    {
        var sessionParamType = info.Methods
            .Where(m => m.HasSession)
            .Select(m => m.Symbol.Parameters.FirstOrDefault(p => IsQuerySession(p.Type))?.Type)
            .FirstOrDefault(t => t != null);

        if (sessionParamType != null)
        {
            sb.AppendLine($"        var session = ({Fqn(sessionParamType)})sessionInput;");
        }
    }

    private static string ContainerTypeParams(INamedTypeSymbol container)
    {
        if (container.TypeParameters.Length == 0) return string.Empty;
        return "<" + string.Join(", ", container.TypeParameters.Select(t => t.Name)) + ">";
    }

    /// <summary>
    /// Returns the type-declaration keyword that matches the user's source.
    /// Required when a projection class is nested inside a record (a fairly
    /// common pattern, e.g. `record Foo { partial class Projector ... }`)
    /// — emitting `partial class Foo` against a `partial record Foo` fails
    /// the build with CS0102 ("already contains a definition").
    /// </summary>
    private static string ContainerKeyword(INamedTypeSymbol container)
    {
        if (container.IsRecord)
        {
            return container.TypeKind == TypeKind.Struct ? "record struct" : "record";
        }

        return container.TypeKind == TypeKind.Struct ? "struct" : "class";
    }

    public static string EmitEventProjectionPartial(CandidateInfo info)
    {
        var sb = new StringBuilder();
        AppendGeneratedFileHeader(sb);

        var ns = info.ClassSymbol.ContainingNamespace;
        if (!ns.IsGlobalNamespace)
        {
            sb.AppendLine($"namespace {ns.ToDisplayString()};");
            sb.AppendLine();
        }

        // Handle nested types by wrapping in containing type declarations
        var containingTypes = GetContainingTypes(info.ClassSymbol);
        foreach (var container in containingTypes)
        {
            sb.AppendLine($"partial {ContainerKeyword(container)} {container.Name}{ContainerTypeParams(container)}");
            sb.AppendLine("{");
        }

        var className = info.ClassSymbol.Name;

        // Include type parameters if the class is generic
        var typeParams = "";
        if (info.ClassSymbol.TypeParameters.Length > 0)
        {
            typeParams = "<" + string.Join(", ", info.ClassSymbol.TypeParameters.Select(t => t.Name)) + ">";
        }

        sb.AppendLine($"partial class {className}{typeParams}");
        sb.AppendLine("{");

        // Emit constructor that registers published document types from Create/Transform methods
        EmitEventProjectionConstructor(sb, info, className);

        EmitEventProjectionApplyAsync(sb, info);

        sb.AppendLine("}");

        // Close containing type declarations
        foreach (var _ in containingTypes)
        {
            sb.AppendLine("}");
        }

        return sb.ToString();
    }

    /// <summary>
    /// Emits a partial class with only a constructor that registers published document types.
    /// Used for EventProjection subclasses that have an explicit ApplyAsync override and
    /// call Store/Insert/Delete with document types that need to be registered.
    /// See https://github.com/JasperFx/marten/issues/4166
    /// </summary>
    public static string EmitEventProjectionTypeRegistrationPartial(CandidateInfo info)
    {
        var sb = new StringBuilder();
        AppendGeneratedFileHeader(sb);

        var ns = info.ClassSymbol.ContainingNamespace;
        if (!ns.IsGlobalNamespace)
        {
            sb.AppendLine($"namespace {ns.ToDisplayString()};");
            sb.AppendLine();
        }

        // Handle nested types
        var containingTypes = GetContainingTypes(info.ClassSymbol);
        foreach (var container in containingTypes)
        {
            sb.AppendLine($"partial {ContainerKeyword(container)} {container.Name}{ContainerTypeParams(container)}");
            sb.AppendLine("{");
        }

        var className = info.ClassSymbol.Name;

        var typeParams = "";
        if (info.ClassSymbol.TypeParameters.Length > 0)
        {
            typeParams = "<" + string.Join(", ", info.ClassSymbol.TypeParameters.Select(t => t.Name)) + ">";
        }

        sb.AppendLine($"partial class {className}{typeParams}");
        sb.AppendLine("{");

        if (!info.HasExistingParameterlessConstructor && info.DiscoveredPublishedTypes.Count > 0)
        {
            sb.AppendLine($"    [global::System.CodeDom.Compiler.GeneratedCodeAttribute(\"JasperFx.Events.SourceGenerator\", \"1.0\")]");
            sb.AppendLine($"    public {className}()");
            sb.AppendLine("    {");
            foreach (var docType in info.DiscoveredPublishedTypes.Select(t => Fqn(t)).Distinct())
            {
                sb.AppendLine($"        RegisterPublishedType(typeof({docType}));");
            }
            sb.AppendLine("    }");
        }

        sb.AppendLine("}");

        foreach (var _ in containingTypes)
        {
            sb.AppendLine("}");
        }

        return sb.ToString();
    }

    private static List<INamedTypeSymbol> GetContainingTypes(INamedTypeSymbol type)
    {
        var result = new List<INamedTypeSymbol>();
        var current = type.ContainingType;
        while (current != null)
        {
            result.Add(current);
            current = current.ContainingType;
        }

        result.Reverse(); // Outermost first
        return result;
    }

    /// <summary>
    /// Emits a constructor that registers document types published by Create/Transform methods
    /// as published types, so that the document storage is generated for code-gen scenarios.
    /// See https://github.com/JasperFx/marten/issues/4166
    /// </summary>
    private static void EmitEventProjectionConstructor(StringBuilder sb, CandidateInfo info, string className)
    {
        // Skip if the class already has an explicit parameterless constructor
        if (info.HasExistingParameterlessConstructor) return;

        // Collect distinct entity return types from Create/Transform methods
        var publishedTypes = info.Methods
            .Where(m => m.EntityReturnType != null && (m.MethodName == "Create" || m.MethodName == "Transform"))
            .Select(m => Fqn(m.EntityReturnType!))
            .Distinct()
            .ToList();

        if (publishedTypes.Count == 0) return;

        sb.AppendLine($"    [global::System.CodeDom.Compiler.GeneratedCodeAttribute(\"JasperFx.Events.SourceGenerator\", \"1.0\")]");
        sb.AppendLine($"    public {className}()");
        sb.AppendLine("    {");
        foreach (var typeName in publishedTypes)
        {
            sb.AppendLine($"        RegisterPublishedType(typeof({typeName}));");
        }
        sb.AppendLine("    }");
        sb.AppendLine();
    }

    private static void EmitEventProjectionApplyAsync(StringBuilder sb, CandidateInfo info)
    {
        var operationsType = Fqn(info.OperationsType!);
        var hasAnyAsync = info.HasAnyAsync;

        // Sort methods so that derived event types come before base event types
        // to avoid unreachable switch case errors
        var sortedMethods = info.Methods
            .OrderByDescending(m => GetTypeDepth(m.EventType))
            .ToList();

        if (hasAnyAsync)
        {
            sb.AppendLine("    [global::System.CodeDom.Compiler.GeneratedCodeAttribute(\"JasperFx.Events.SourceGenerator\", \"1.0\")]");
            sb.AppendLine($"    public override async global::System.Threading.Tasks.ValueTask ApplyAsync(");
            sb.AppendLine($"        {operationsType} operations,");
            sb.AppendLine($"        global::JasperFx.Events.IEvent e,");
            sb.AppendLine($"        global::System.Threading.CancellationToken cancellation)");
            sb.AppendLine("    {");
            sb.AppendLine("        switch (e.Data)");
            sb.AppendLine("        {");

            foreach (var method in sortedMethods)
            {
                EmitEventProjectionCase(sb, method, operationsType, true);
            }

            sb.AppendLine("        }");
            sb.AppendLine("    }");
        }
        else
        {
            sb.AppendLine("    [global::System.CodeDom.Compiler.GeneratedCodeAttribute(\"JasperFx.Events.SourceGenerator\", \"1.0\")]");
            sb.AppendLine($"    public override global::System.Threading.Tasks.ValueTask ApplyAsync(");
            sb.AppendLine($"        {operationsType} operations,");
            sb.AppendLine($"        global::JasperFx.Events.IEvent e,");
            sb.AppendLine($"        global::System.Threading.CancellationToken cancellation)");
            sb.AppendLine("    {");
            sb.AppendLine("        switch (e.Data)");
            sb.AppendLine("        {");

            foreach (var method in sortedMethods)
            {
                EmitEventProjectionCase(sb, method, operationsType, false);
            }

            sb.AppendLine("        }");
            sb.AppendLine("        return new global::System.Threading.Tasks.ValueTask();");
            sb.AppendLine("    }");
        }
    }

    /// <summary>
    /// Picks one handler per event type so the generated switch doesn't emit
    /// duplicate arms (CS8120). When the SG discovers both a projection-side
    /// handler (e.g. `Bug4268ProductProjection.Apply(EventX, AggregateY)`)
    /// AND an aggregate-side handler for the same event type (e.g.
    /// `Bug4268Product.Apply(EventX)`), the projection-side handler wins —
    /// the projection's method typically wraps / extends the aggregate's
    /// behavior and is the user's intentional dispatch entry point. Ties on
    /// IsOnAggregate break to the overload with more parameters (the more
    /// specific signature). Final ordering puts more-derived event types
    /// first so interface arms don't shadow concrete-class arms.
    /// </summary>
    private static List<ConventionalMethodInfo> CoalesceByEventType(IEnumerable<ConventionalMethodInfo> methods)
    {
        return methods
            .GroupBy<ConventionalMethodInfo, ITypeSymbol>(m => m.EventType, SymbolEqualityComparer.Default)
            .Select(g => g
                .OrderBy(m => m.IsOnAggregate)
                .ThenByDescending(m => m.Symbol.Parameters.Length)
                .First())
            .OrderByDescending(m => GetTypeDepth(m.EventType))
            .ToList();
    }

    private static int GetTypeDepth(ITypeSymbol type)
    {
        // Used to order `switch (e.Data)` arms so that more-derived types come
        // before their base types / interfaces. C# pattern matching falls
        // through to the first satisfied case; if `case IBase data` is emitted
        // before `case Concrete data` and Concrete : IBase, the compiler flags
        // the latter as unreachable (CS8120).
        //
        // Interfaces are pushed below every concrete class (negative key) so
        // an interface arm always sinks below any class arm — including arms
        // for classes that *don't* implement the interface, which is fine
        // because order among unrelated types doesn't matter to correctness.
        // Among interfaces, ones that extend more other interfaces sort first.
        if (type.TypeKind == TypeKind.Interface)
        {
            return -1_000_000 + type.AllInterfaces.Length;
        }

        int depth = 0;
        var current = type.BaseType;
        while (current != null)
        {
            depth++;
            current = current.BaseType;
        }

        return depth;
    }

    private static void EmitEventProjectionCase(StringBuilder sb, ConventionalMethodInfo method,
        string operationsType, bool isAsyncContext)
    {
        var eventTypeName = Fqn(method.EventType);

        if (method.UsesIEventWrapper)
        {
            sb.AppendLine($"            case {eventTypeName}:");
        }
        else
        {
            sb.AppendLine($"            case {eventTypeName} data:");
        }

        if (method.MethodName == "Project")
        {
            EmitEventProjectionProjectCall(sb, method, isAsyncContext);
        }
        else // Create or Transform
        {
            EmitEventProjectionCreateCall(sb, method, isAsyncContext);
        }

        sb.AppendLine("                break;");
    }

    private static void EmitEventProjectionProjectCall(StringBuilder sb, ConventionalMethodInfo method,
        bool isAsyncContext)
    {
        var args = BuildEventProjectionArgs(method);

        if (method.IsAsync && isAsyncContext)
        {
            sb.AppendLine($"                await {method.MethodName}({args});");
        }
        else
        {
            sb.AppendLine($"                {method.MethodName}({args});");
        }
    }

    private static void EmitEventProjectionCreateCall(StringBuilder sb, ConventionalMethodInfo method,
        bool isAsyncContext)
    {
        var args = BuildEventProjectionArgs(method);

        if (method.IsAsync && isAsyncContext)
        {
            sb.AppendLine($"                storeEntity(operations, await {method.MethodName}({args}));");
        }
        else
        {
            sb.AppendLine($"                storeEntity(operations, {method.MethodName}({args}));");
        }
    }

    private static string BuildEventProjectionArgs(ConventionalMethodInfo method)
    {
        var parts = new List<string>();
        var parameters = method.Symbol.Parameters;

        foreach (var param in parameters)
        {
            var paramType = param.Type;

            if (method.UsesIEventWrapper && IsIEventGenericWrapper(paramType))
            {
                var innerType = Fqn(((INamedTypeSymbol)paramType).TypeArguments[0]);
                parts.Add($"(global::JasperFx.Events.IEvent<{innerType}>)e");
            }
            else if (IsRawIEvent(paramType))
            {
                parts.Add("e");
            }
            else if (IsCancellationToken(paramType))
            {
                parts.Add("cancellation");
            }
            else if (IsEventDataType(paramType, method))
            {
                if (method.UsesIEventWrapper)
                {
                    parts.Add($"(({Fqn(paramType)})e.Data)");
                }
                else
                {
                    parts.Add("data");
                }
            }
            else
            {
                // Assume it's the operations type
                parts.Add("operations");
            }
        }

        return string.Join(", ", parts);
    }

