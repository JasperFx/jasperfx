        sb.AppendLine("                default: return null;");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        switch (e.Data)");
        sb.AppendLine("        {");
        EmitNonNullSnapshotCasesAsync(sb, info, applyMethods, "            ");
        sb.AppendLine("            default: return snapshot;");
        sb.AppendLine("        }");

        sb.AppendLine("    }");
    }

    // --- Partial projection: DetermineActionAsync (has ShouldDelete) ---

    private static void EmitDetermineActionEvolverMethod(StringBuilder sb, CandidateInfo info)
    {
        var docType = Fqn(info.AggregateType!);
        var idType = Fqn(info.IdentityType!);

        var isAsync = info.HasAnyAsync;
        var asyncKeyword = isAsync ? "async " : "";

        sb.AppendLine($"    public {asyncKeyword}global::System.Threading.Tasks.ValueTask<({docType}?, global::JasperFx.Events.Daemon.ActionType)> DetermineActionAsync(");
        sb.AppendLine($"        {docType}? snapshot,");
        sb.AppendLine($"        {idType} id,");
        sb.AppendLine($"        global::System.Collections.Generic.IReadOnlyList<global::JasperFx.Events.IEvent> events,");
        sb.AppendLine("        object sessionInput,");
        sb.AppendLine("        global::System.Threading.CancellationToken cancellation)");
        sb.AppendLine("    {");
        EmitSessionLocalIfNeeded(sb, info);
        sb.AppendLine("        var exists = snapshot != null;");
        sb.AppendLine("        foreach (var e in events)");
        sb.AppendLine("        {");
        // Per-event try/catch so a poison-pill Apply/Create/ShouldDelete is re-raised
        // as ApplyEventException carrying *that* event. The daemon's buildBatchWithSkipping
        // catch handler keys off ApplyEventException.Event.Sequence to route the failing
        // event to the dead-letter queue — without this wrap the daemon sees the raw
        // user exception and SkipApplyErrors can't isolate the offending event.
        // Matches evolveDefaultAsync's semantics in JasperFxAggregationProjectionBase.Runtime.cs.
        // Already-ApplyEventException-typed throws (e.g. user code raised it explicitly)
        // propagate unchanged so we don't double-wrap. See #305.
        sb.AppendLine("            try");
        sb.AppendLine("            {");
        sb.AppendLine("            switch (e.Data)");
        sb.AppendLine("            {");

        // ShouldDelete cases first — coalesce overloads sharing the same event
        // type into a single switch arm so the compiler doesn't flag duplicate
        // arms as unreachable (CS8120). Multiple ShouldDelete overloads on the
        // same event type are OR'd together so the snapshot is dropped if any
        // overload returns true.
        var shouldDeleteMethods = info.Methods.Where(m => m.MethodName == "ShouldDelete").ToList();
        var shouldDeleteByEventType = shouldDeleteMethods
            .GroupBy<ConventionalMethodInfo, ITypeSymbol>(m => m.EventType, SymbolEqualityComparer.Default)
            .OrderByDescending(g => GetTypeDepth(g.Key))
            .ToList();
        // Constructor-side `DeleteEvent<T>()` registrations — the kept Marten
        // 9.0 API for "this event type always deletes the aggregate." Emit one
        // arm per event type that sets `snapshot = null` unconditionally.
        // Skip types that already have a ShouldDelete handler so we don't emit
        // duplicate switch arms (CS8120). See #297.
        var shouldDeleteEventTypes = new HashSet<ITypeSymbol>(
            shouldDeleteMethods.Select(m => m.EventType), SymbolEqualityComparer.Default);
        var ctorDeleteEventTypes = info.ConstructorDeleteEventTypes
            .Where(t => !shouldDeleteEventTypes.Contains(t))
            .Distinct<ITypeSymbol>(SymbolEqualityComparer.Default)
            .OrderByDescending(t => GetTypeDepth(t))
            .ToList();
        foreach (var group in shouldDeleteByEventType)
        {
            var eventTypeName = Fqn(group.Key);
            var dataVar = "data";
            sb.AppendLine($"                case {eventTypeName} {dataVar}:");
            sb.Append("                    if (snapshot != null && (");
            bool first = true;
            foreach (var method in group)
            {
                if (!first) sb.Append(" || ");
                first = false;
                EmitShouldDeleteCall(sb, method, dataVar);
            }
            sb.AppendLine("))");
            sb.AppendLine("                        snapshot = null;");
            sb.AppendLine("                    break;");
        }
        foreach (var eventType in ctorDeleteEventTypes)
        {
            sb.AppendLine($"                case {Fqn(eventType)}:");
            sb.AppendLine("                    snapshot = null;");
            sb.AppendLine("                    break;");
        }

        // Create cases — coalesce by event type, prefer projection-defined over aggregate-defined.
        var createMethods = CoalesceByEventType(info.Methods.Where(m => m.MethodName == "Create"));
        foreach (var method in createMethods)
        {
            var eventTypeName = Fqn(method.EventType);
            sb.AppendLine($"                case {eventTypeName} data when snapshot == null:");
            sb.Append("                    snapshot = ");
            EmitCreateCall(sb, method, "data", "e", isAsync);
            sb.AppendLine(";");
            sb.AppendLine("                    break;");
        }

        // Apply cases — coalesce by event type.
        var applyMethods = CoalesceByEventType(info.Methods.Where(m => m.MethodName == "Apply"));
        foreach (var method in applyMethods)
        {
            var eventTypeName = Fqn(method.EventType);
            // For apply methods where there's no create, we need to handle null snapshot
            bool hasCreateForType = createMethods.Any(c =>
                SymbolEqualityComparer.Default.Equals(c.EventType, method.EventType));

            if (hasCreateForType)
            {
                // Apply only when snapshot exists
                sb.AppendLine($"                case {eventTypeName} data when snapshot != null:");
            }
            else if (info.HasDefaultConstructor)
            {
                sb.AppendLine($"                case {eventTypeName} data:");
                sb.AppendLine($"                    snapshot ??= {BuildAggregateConstructorExpression(info.AggregateType!)};");
            }
            else
            {
                sb.AppendLine($"                case {eventTypeName} data when snapshot != null:");
            }

            sb.Append("                    ");
            EmitApplyCallStatement(sb, method, "data", "e", isAsync);
            sb.AppendLine("                    break;");
        }

        sb.AppendLine("            }");
        sb.AppendLine("            }");
        sb.AppendLine("            catch (global::JasperFx.Events.Daemon.ApplyEventException)");
        sb.AppendLine("            {");
        sb.AppendLine("                throw;");
        sb.AppendLine("            }");
        sb.AppendLine("            catch (global::System.Exception __ex)");
        sb.AppendLine("                when (!global::JasperFx.Events.Projections.ProjectionExceptions.IsExceptionTransient(__ex))");
        sb.AppendLine("            {");
        sb.AppendLine("                throw new global::JasperFx.Events.Daemon.ApplyEventException(e, __ex);");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();
        sb.AppendLine("        if (snapshot == null)");
        if (!isAsync)
        {
            // Sync-path DetermineActionAsync returns ValueTask<(...)> directly; wrap the tuple.
            sb.AppendLine($"            return exists ? new global::System.Threading.Tasks.ValueTask<({docType}?, global::JasperFx.Events.Daemon.ActionType)>((null, global::JasperFx.Events.Daemon.ActionType.Delete)) : new global::System.Threading.Tasks.ValueTask<({docType}?, global::JasperFx.Events.Daemon.ActionType)>((null, global::JasperFx.Events.Daemon.ActionType.Nothing));");
            sb.AppendLine($"        return new global::System.Threading.Tasks.ValueTask<({docType}?, global::JasperFx.Events.Daemon.ActionType)>((snapshot, global::JasperFx.Events.Daemon.ActionType.Store));");
        }
        else
        {
            // Async method body — bare tuple is implicitly wrapped by the async state machine.
            sb.AppendLine($"            return exists ? (null, global::JasperFx.Events.Daemon.ActionType.Delete) : (null, global::JasperFx.Events.Daemon.ActionType.Nothing);");
            sb.AppendLine($"        return (snapshot, global::JasperFx.Events.Daemon.ActionType.Store);");
        }
        sb.AppendLine("    }");
    }

    // --- Self-aggregating: Evolve (sync, no ShouldDelete) ---

    private static void EmitSelfAggregatingEvolve(StringBuilder sb, CandidateInfo info, string docType,
        string idType)
    {
        sb.AppendLine(
            $"    public {docType}? Evolve({docType}? snapshot, {idType} id, global::JasperFx.Events.IEvent e)");
        sb.AppendLine("    {");
        sb.AppendLine("        switch (e.Data)");
        sb.AppendLine("        {");

        var createMethods = info.Methods.Where(m => m.MethodName == "Create").ToList();
        var applyMethods = info.Methods.Where(m => m.MethodName == "Apply").ToList();

        // Each event type gets one case. Include event types referenced by
        // event-shaped public constructors (Bucket 1) so e.g.
        // `public Invoice(InvoiceCreated e)` becomes a dispatch arm with
        // `snapshot = new Invoice(data);` in the null-snapshot branch.
        // Sort more-derived types first so `case IBase data:` doesn't shadow
        // `case Concrete data:` (CS8120).
        var allEventTypes = info.Methods
            .Select(m => m.EventType)
            .Concat(info.EventConstructors.Select(c => c.EventType))
            .Distinct<ITypeSymbol>(SymbolEqualityComparer.Default)
            .OrderByDescending(t => GetTypeDepth(t!))
            .ToList();

        foreach (var eventType in allEventTypes)
        {
            var eventTypeName = Fqn(eventType!);
            var creates = createMethods.Where(m =>
                SymbolEqualityComparer.Default.Equals(m.EventType, eventType)).ToList();
            var applies = applyMethods.Where(m =>
                SymbolEqualityComparer.Default.Equals(m.EventType, eventType)).ToList();
            var eventCtor = info.EventConstructors.FirstOrDefault(c =>
                SymbolEqualityComparer.Default.Equals(c.EventType, eventType));

            sb.AppendLine($"            case {eventTypeName} data:");

            if (creates.Count > 0)
            {
                var create = creates[0];
                sb.AppendLine("                if (snapshot == null)");
                sb.Append("                    snapshot = ");
                EmitSelfAggregatingCreateCall(sb, create, "data");
                sb.AppendLine(";");

                if (applies.Count > 0)
                {
                    sb.AppendLine("                else");
                    sb.Append("                    ");
                    EmitSelfAggregatingApplyCall(sb, applies[0], "data");
                }
            }
            else if (eventCtor != null)
            {
                // Implicit Create via public T(EventType) constructor.
                sb.AppendLine("                if (snapshot == null)");
                sb.AppendLine($"                    snapshot = new {Fqn(info.AggregateType!)}(data);");

                if (applies.Count > 0)
                {
                    sb.AppendLine("                else");
                    sb.Append("                    ");
                    EmitSelfAggregatingApplyCall(sb, applies[0], "data");
                }
            }
            else if (applies.Count > 0)
            {
                if (info.HasDefaultConstructor)
                {
                    sb.AppendLine($"                snapshot ??= {BuildAggregateConstructorExpression(info.AggregateType!)};");
                }
                else
                {
                    sb.AppendLine("                if (snapshot == null) return null;");
                }

                sb.Append("                ");
                EmitSelfAggregatingApplyCall(sb, applies[0], "data");
            }

            sb.AppendLine("                return snapshot;");
        }

        sb.AppendLine("            default:");
        sb.AppendLine("                return snapshot;");
        sb.AppendLine("        }");
        sb.AppendLine("    }");
    }

